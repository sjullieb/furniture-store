export class Item{
  constructor (id, name, imageUrl, description, colors, type, deliverable, dimentions, cost, stock){
    this.id = id;
    this.name = name;
    this.imageUrl = imageUrl;
    this.description = description;
    this.colors = colors;
    this.type = type;
    this.deliverable = deliverable;
    this.dimentions = dimentions;
    this.cost = cost;
    this.stock = stock;
  }
}